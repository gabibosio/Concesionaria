    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(tabla.getRowCount() > 0){
            if(tabla.getSelectedRow() != -1){
                int id = Integer.parseInt(String.valueOf(tabla.getValueAt(tabla.getSelectedRow(),0)));
                controladora.eliminarAuto(id);
                
                mensaje("Automovil eliminado correctamente","Automovil eliminado","info");
                cargarTabla();
            }
            else{   
                mensaje("Seleccione un automovil","Automovil no seleccionado","error");
            }
        }
        else{
            mensaje("No hay Autos","Error no hay auto para eliminar","error");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    public void mensaje(String mensaje,String titulo,String tipo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }else{
            if(tipo.equals("error")){
                optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
            }
        }
                   
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }

