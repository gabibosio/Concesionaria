    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

         if(tabla.getRowCount() > 0){
            if(tabla.getSelectedRow() != -1){
                int id = Integer.parseInt(String.valueOf(tabla.getValueAt(tabla.getSelectedRow(),0)));
                    EditarDatos editar = new EditarDatos(id);
                    editar.setVisible(true);
                    editar.setLocationRelativeTo(null);
                    this.dispose();
            }
            else{   
                mensaje("Seleccione un automovil","Automovil no seleccionado","error");
            }
        }
        else{
            mensaje("No hay Autos","Error no hay auto para editar","error");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

