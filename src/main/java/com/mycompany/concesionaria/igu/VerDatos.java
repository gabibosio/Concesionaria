    private void cargarTabla() {
        DefaultTableModel tablaModelo = new DefaultTableModel(){
        
        @Override
        public boolean isCellEditable (int row, int column){
            return false;
        }
    };
        
        String titulos[] = {"id","Modelo","Marca","Motor","Color","Patente","Cant.Puertas"};
        tablaModelo.setColumnIdentifiers(titulos);
        
        List<Automovil> listaAutos = controladora.traerAutos();
        
        if(listaAutos != null){        
        for(Automovil auto : listaAutos){
            Object[] objeto = {auto.getId(),auto.getModelo(),auto.getMarca(),auto.getMotor(),auto.getColor(),auto.getPatente(),auto.getCantidadPuertas()};
            tablaModelo.addRow(objeto);
        }
       }
        tabla.setModel(tablaModelo);
        
    }
